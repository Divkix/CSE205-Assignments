// Node class
public class Node {
    // instance variables
    private int x;
    private int y;

    // constructor
    public Node(int x, int y) {
        this.x = x;
        this.y = y;
    }

    // get the X point from the node
    public int getX() {
        return x;
    }

    // get the Y point from the node
    public int getY() {
        return y;
    }
}
